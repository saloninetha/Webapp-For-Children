document.getElementById('chapters1').addEventListener('change', function() {
    var selectedValue = this.value;
    if (selectedValue === 'Module 1') {
        window.location.href = 'Module 1.html'; 
    } else if (selectedValue === 'Module 2') {
        window.location.href = 'Module 2.html';
    } 
});

